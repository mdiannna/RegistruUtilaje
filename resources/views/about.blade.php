   <p><b>Registrul Digital al Utilajelor</b> este un proiect inițiat de către Re:Rise (Asociația pentru Reducerea Riscului Seismic) care pune la dispoziția DSU (Departamentul pentru Situații de Urgență - principala autoritate responsabilă pentru prevenirea și gestionarea situațiilor de urgență la nivel național) o bază de date cu utilajele deținute de către operatori privați (buldozere, excavatoare etc.), care să ajute la răspunsul post-seism al forțelor de salvare.
